  String getAward(int totalScore) {
    if (totalScore >= standards['1']['gold']) {
      return 'Gold';
    } else if (totalScore >= standards['1']['silver']) {
      return 'Silver';
    } else if (totalScore >= standards['1']['pass']) {
      return 'Pass';
    } else {
      return 'Fail';
    }
  }

  // Get total score
  int getOverallScore() {
    String pScore = getScore(pushup, "push-up");
    String sScore = getScore(situp, "sit-up");
    String rScore = getScore(run, "run");
    return int.parse(pScore) + int.parse(sScore) + int.parse(rScore);
  }
